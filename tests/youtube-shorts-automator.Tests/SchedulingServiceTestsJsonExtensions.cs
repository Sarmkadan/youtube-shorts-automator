using System.Text.Json;

namespace YouTubeShortAutomator.Tests;

/// <summary>
/// Provides JSON serialization and deserialization extensions for <see cref="SchedulingServiceTests"/>.
/// </summary>
public static class SchedulingServiceTestsJsonExtensions
{
    private static readonly JsonSerializerOptions _jsonSerializerOptions = new(JsonSerializerDefaults.Web)
    {
        PropertyNamingPolicy = JsonNamingPolicy.CamelCase,
        WriteIndented = false
    };

    /// <summary>
    /// Serializes the <see cref="SchedulingServiceTests"/> instance to a JSON string.
    /// </summary>
    /// <param name="value">The instance to serialize.</param>
    /// <param name="indented">Whether to format the JSON with indentation.</param>
