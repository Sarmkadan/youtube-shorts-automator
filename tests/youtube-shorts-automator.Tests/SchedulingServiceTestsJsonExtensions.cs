        }

        return JsonSerializer.Deserialize<SchedulingServiceTests>(json, _jsonSerializerOptions);
    }

    /// <summary>
    /// Attempts to deserialize a JSON string to a <see cref="SchedulingServiceTests"/> instance.
    /// </summary>
    /// <param name="json">The JSON string to deserialize.</param>
    /// <param name="value">Receives the deserialized instance if successful.</param>
    /// <returns>True if deserialization succeeded; otherwise, false.</returns>
    /// <exception cref="ArgumentNullException">Thrown when <paramref name="json"/> is null.</exception>
    public static bool TryFromJson(string json, out SchedulingServiceTests? value)
    {
        ArgumentNullException.ThrowIfNull(json);

        value = default;

        try
        {
            if (string.IsNullOrWhiteSpace(json))
            {
                return true;
            }

            value = JsonSerializer.Deserialize<SchedulingServiceTests>(json, _jsonSerializerOptions);
